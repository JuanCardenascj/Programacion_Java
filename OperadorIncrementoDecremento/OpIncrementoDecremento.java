package OperadorIncrementoDecremento;

public class OpIncrementoDecremento {

    public static void main(String[] args) {
        int x = 5;
        x ++; //Aumenta de uno en uno, aumenta uno a 5.

        int y = 19;
        y--; //Operador decremento

        //Crea dos Variables
        int a = 5, b;
        b = a++; //Le estamos asignado el valor de a= 5 a b=5
        //Si imprimimos  a seria igual a 6

        int c = 4, v;
        v = c--; //Decremento

    }
}
