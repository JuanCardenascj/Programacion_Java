public class Operadores_Comparacion {

    /*Sirven para comparar valores o situaciones o condiciones*/

    public static void main(String[] args) {

        /*Creamos Variables.!*/

        int valor1 = 90, valor2 = 50;

        /*Operador de Igualdad*/
        System.out.println(valor1 == valor2); //Verifica si valor1 es igual a valor2

        /*Operador de Distinto a o de*/
        System.out.println(valor1 != valor2); //Verifica si el valor1 es difirente a valor2

        /*Operador Mayor que*/
        System.out.println(valor1 > valor2); //Verifica si el valor1 es mayor que el valor2

        /*Operador Menor que*/
        System.out.println(valor1 < valor2); //Verifica si el valor1 es menor que el valor2

        /*Operador Mayor o igual*/
        System.out.println(valor1 >= valor2); //Verifica si el valor1 es mayor o igual que el valor2

        /*Operador Menor o igual*/
        System.out.println(valor1 <= valor2); //Verifica si el valor1 es menor o igual que el valor2


    }
}
