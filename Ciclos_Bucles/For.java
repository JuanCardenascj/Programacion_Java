package Ciclos_Bucles;

/*Ciclo For
*
* for(inicializacion; condicion; aumento o decremento) {
*     Instrucciones;
*    }
* */

public class For {
    public static void main(String[] args) {
        //Ejemplo
        for (int i=1; i<=10; i++) {
            System.out.println(i);

        }

    }
}
