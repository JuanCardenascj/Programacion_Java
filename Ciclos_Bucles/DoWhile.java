package Ciclos_Bucles;

/*Ciclo do While

    do {
        Instruccines;
    } while (condicion);
* */

public class DoWhile {
    public static void main(String[] args) {

        //Imprime numeros del 1 al 10
        int i=1;

        do {
            System.out.println(i);
            i++;
        } while (i<=10);
    }

    int i=10;

}
