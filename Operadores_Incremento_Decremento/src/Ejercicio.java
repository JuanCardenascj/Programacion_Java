public class Ejercicio {

    /*Crear un programa donde se evidencie la utilizacion
    * de los Operadores de Incremento y Decremento*/

    public static void main(String[] args) {
        int valor1 = 100;
        System.out.println("Valor Originial:" + valor1);

        valor1++;
        System.out.println("Incremento: " + valor1);

        valor1--;
        System.out.println("Decremento: " +valor1);

        valor1*=1;
        System.out.println("Decremento: " +valor1);

        valor1/=1;
        System.out.println("Decremento: " +valor1);

    }
}
