public class Incremendo_Decremento {

    public static void main(String[] args){

        System.out.println("INCREMENTO FIJO SUMA....!");

        /*Creamos una variable*/
        int valor1 = 5;
        System.out.println("Valor original: " + valor1);

        /*Incrementando valor1 POST FIJO*/
        valor1++; //Incrementa de uno en uno
        System.out.println("Valor incrementado: " + valor1);

        /*Incrementando valor1 POST FIJO*/
        valor1+=2; //Incrementa de dos en dos
        System.out.println("Valor incrementado: " + valor1);

        /*Incrementando valor1 POST FIJO*/
        valor1+=3; //Incrementa de tres en tres
        System.out.println("Valor incrementado: " + valor1);

        /*Incrementando valor1 POST FIJO*/
        valor1+=4; //Incrementa de cuatro en cuatro
        System.out.println("Valor incrementado: " + valor1);

        System.out.println(" ");
        System.out.println("DECREMENTO FIJO RESTA...!");

        /*Creamos una variable*/
        int valor2 = 10;
        System.out.println("Valor original: " + valor2);

        /*Decremento valor2 POST FIJO*/
        valor2--; //Decrementamos de uno en uno
        System.out.println("Valor Decremento: "+ valor2);

        /*Decremento valor2 POST FIJO*/
        valor2-=2; //Decrementamos de dos en dos
        System.out.println("Valor Decremento: "+ valor2);

        /*Decremento valor2 POST FIJO*/
        valor2-=3; //Decrementamos de tres en tres
        System.out.println("Valor Decremento: "+ valor2);

        /*Decremento valor2 POST FIJO*/
        valor2-=4; //Decrementamos de cuatro en cuatro
        System.out.println("Valor Decremento: "+ valor2);

        System.out.println(" ");
        System.out.println("INCREMENTO FIJO MULTIPLICACION....!");

        /*Creamos una variable*/
        int valor3 = 20;
        System.out.println("Valor original: " + valor3);

        /*Incremento POST FIJO*/
        valor3*=2; //Incrementado *2
        System.out.println("Valor Incrementado: " + valor3);

        /*Incremento POST FIJO*/
        valor3*=3; //Incrementado *3
        System.out.println("Valor Incrementado: " + valor3);

        /*Incremento POST FIJO*/
        valor3*=4; //Incrementado *4
        System.out.println("Valor Incrementado: " + valor3);

        /*Incremento POST FIJO*/
        valor3*=5; //Incrementado *5
        System.out.println("Valor Incrementado: " + valor3);

        System.out.println(" ");
        System.out.println("INCREMENTO FIJO DIVISION....!");

        /*Creamos una variable*/
        int valor4 = 50;
        System.out.println("Valor original: " + valor4);

        /*Incremento POST FIJO*/
        valor4/=2; //Incrementado /2
        System.out.println("Valor Incrementado: " + valor4);

        /*Incremento POST FIJO*/
        valor4/=3; //Incrementado /3
        System.out.println("Valor Incrementado: " + valor4);

        /*Incremento POST FIJO*/
        valor4/=4; //Incrementado /4
        System.out.println("Valor Incrementado: " + valor4);

        /*Incremento POST FIJO*/
        valor4/=5; //Incrementado /5
        System.out.println("Valor Incrementado: " + valor4);


    }
}
