public class Constantes {

    /*Son valores fijos e inalterables en los programas*/

    public static void main(String[] args) {

        /*Variables constantes - No se puede modificar su valor*/
        final int valor = 50; //FINAL es para definirla como constante
        System.out.println(valor);
        // valor = 400; Si intentamos cambiar el valor, este no lo permite, sale error

        /*QUE TIPOS DE VARIABLES DEBEMOS DECLARAR COMO CONSTANTES
        * Valores Aritmeticos
        * Configuracion y parametros
        * Limites o rangos
        * Nombres de dias de la semana, meses
        * Codigo de error o codigos de estado*/
    }
}
